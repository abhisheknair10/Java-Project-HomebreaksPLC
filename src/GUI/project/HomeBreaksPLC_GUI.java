    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        boolean error = false;

        String DISPLAYNAME = displayName.getText();

        String TITLE = (String) title.getSelectedItem();
        TITLE = String.valueOf(title.getSelectedItem());

        String FORENAME = forename.getText();
        String SURNAME = surname.getText();
        String EMAIL = email.getText();
        String PASSWORD = String.valueOf(rawPassword.getPassword());
        String CONFADDRESS = confAddress.getText();
        String PHONENO = phoneNo.getText();

        signUpMethods validators = new signUpMethods();

        if(!validators.emailValidator(EMAIL)) {
            showMessageDialog(null, "Invalid Email Address Format");
            error = true;
        }

        if(DISPLAYNAME.contains(" ") || DISPLAYNAME.isEmpty()){
            showMessageDialog(null, "Display Name must not contain Whitespaces or be an empty value");
            error = true;
        }

        if(FORENAME.contains(" ") || FORENAME.isEmpty()){
            showMessageDialog(null, "Forename must not contain Whitespaces or be an empty value");
            error = true;
        }

        if(SURNAME.contains(" ") || SURNAME.isEmpty()){
            showMessageDialog(null, "Surname must not contain Whitespaces or be an empty value");
            error = true;
        }

        if(PHONENO.contains(" ") || PHONENO.isEmpty()){
            showMessageDialog(null, "Phone Number must not contain Whitespaces or be an empty value");
            error = true;
        }

        String [] CONFADDRESSparts = CONFADDRESS.split(" ");
        CONFADDRESS = String.join("@", CONFADDRESSparts);

        boolean userIDChecker = true;

        if(!error){
            try {
                userIDChecker = validators.checkEmailExists(EMAIL);
            } catch (Exception ex) {
                System.out.println(ex);
            }
            if(!userIDChecker){
                try {
                    validators.createUser(EMAIL, DISPLAYNAME, TITLE, FORENAME, SURNAME, PASSWORD, CONFADDRESS, PHONENO);
                    showMessageDialog(null, "Welcome " + DISPLAYNAME + "");
                    displayName.setText("");
                    title.setSelectedIndex(0);
                    forename.setText("");
                    surname.setText("");
                    email.setText("");
                    rawPassword.setText("");
                    confAddress.setText("");
                    phoneNo.setText("");
                } catch (Exception ex) {
                    System.out.println(ex);
                    showMessageDialog(null, "OOPS! Something Went Wrong");
                }
            }
            else{
                showMessageDialog(null, "An account with this email already exists");
            }
        }
    }//GEN-LAST:event_signUpActionPerformed

